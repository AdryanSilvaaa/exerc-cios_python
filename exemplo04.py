#Funções anominas 

#conhecidas como função lambda, oferecem uma abordagem concisa e flexivel
#para a definição de função pelo usuario.
#funções anonimas são declaradas sem nessecidade de um nome, permitindo 
# que sejam definadas e utilizadas no local em que são necesssario 

soma = lambda a, b : a + b
resultado = soma (3,5)
print(resultado)


